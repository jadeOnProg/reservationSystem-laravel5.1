<h3> About our Church </h3>
<p>We are a non-denominational Christian church at 3210 SE Taylor in Portland, OR and we meet every Sunday at 9am and 11:15am for prayer, music, singing, teaching and fellowship.  </p>

<p>Much more than just Sunday morning services, a large part of our activity as a church is done in Community Groups throughout the week.  As a church, our Community Groups are spread all throughout the greater Portland/Vancouver area. This is where we spend time together, have meals together, discuss the teaching, wrestle with challenges together and lean on one another.</p>

<p>There is always more you can learn about Redeemer Church in a number of areas. If you have a question about something that doesn’t appear here, we hope you visit us Sunday and chat with us. Otherwise contact us online and we’ll be glad to chat.</p>
